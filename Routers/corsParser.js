/* -------------------------- Acquring Dependancies ------------------------- */
const express = require("express");
const cors = require("cors");
/* -------------------------------------------------------------------------- */

/* --------------------------- Creating MiddleWare -------------------------- */
const router = express.Router();
router.use(cors({
    origin: '*'
}));
/* -------------------------------------------------------------------------- */

/* -------------------------- Exporting MiddleWare -------------------------- */
module.exports = router;
/* -------------------------------------------------------------------------- */